window.onscroll = function() {
  if(document.documentElement.scrollTop > 20){ //20-small scroll
    nav.classList.add("sticky");
    scrollBtn.style.display = "block";
  }else{
    nav.classList.remove("sticky");
    scrollBtn.style.display = "none";
  }

}

